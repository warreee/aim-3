	public static int numberOfDifferentParkings = 6;
	// When a tuple has a speed higher than 50 it is counted
	public static int numberOfSpeedOver50 = 18;
	
	@Test
	public void testSpeed() throws Exception {
		final StreamExecutionEnvironment env = StreamExecutionEnvironment.getExecutionEnvironment();
		env.setStreamTimeCharacteristic(TimeCharacteristic.EventTime);
		
		DataStream<Tuple4<Integer, Integer, Double, Long>> input = env
				.fromElements(testData).map(new ParseCarData());
		
		DataStream<Tuple4<Integer, Integer, Double, Long>> result = TaxiMonitorStub
				.higherThan50(input);
		
		//check result
		result.map(new CounterMap());
		JobExecutionResult res = env.execute("Test Speed");
		int counterValue = (Integer) res.getAccumulatorResult("counter");
		assertEquals(numberOfSpeedOver50, counterValue);
	}
	
	@Test
	public void testParking() throws Exception {
		final StreamExecutionEnvironment env = StreamExecutionEnvironment.getExecutionEnvironment();
		env.setStreamTimeCharacteristic(TimeCharacteristic.EventTime);
		env.setParallelism(1);
		
		DataStream<Tuple4<Integer, Integer, Double, Long>> input = env
				.fromElements(testData).map(new ParseCarData());
		
		DataStream<Tuple4<Integer, Integer, Double, Long>> result = TaxiMonitorStub
				.parkingSince5Seconds(input);
		
		//check results
		result.map(new DistancesCounterMap());
		JobExecutionResult res = env.execute("Test Parking");
		@SuppressWarnings("unchecked")
		Set<Double> counterSet = (Set<Double>) res.getAccumulatorResult("counter-set");
		assertEquals(numberOfDifferentParkings, counterSet.size());
	}
	
