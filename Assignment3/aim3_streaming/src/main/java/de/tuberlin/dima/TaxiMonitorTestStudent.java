	/**
	 * Map functions that counts all tuples that are processed by it. The count is stored inside 
	 * the "counter" accumulator, that can be accessed with res.getAccumulatorResult("counter");
	 * This returns an Integer.
	 */
	public static class CounterMap extends RichMapFunction<Tuple4<Integer,Integer,Double,Long>, Tuple4<Integer,Integer,Double,Long>> {
		private static final long serialVersionUID = 1L;

		private IntCounter counter = new IntCounter();
		
		@Override
		public void open(Configuration parameters) {
			this.getRuntimeContext().addAccumulator("counter", counter);
		}
		
		@Override
		public Tuple4<Integer, Integer, Double, Long> map(Tuple4<Integer, Integer, Double, Long> arg0)
				throws Exception {
			counter.add(1);
			return arg0;
		}	
	}
	
	
	/**
	 * Map functions that counts the number of different distances submitted to it. The count is stored inside 
	 * the "counter-set" accumulator, that can be accessed with res.getAccumulatorResult("counter-set");
	 * This returns a Set<Double>, with all different distances stored in that Set.
	 */
	public static class DistancesCounterMap extends RichMapFunction<Tuple4<Integer,Integer,Double,Long>, Tuple4<Integer,Integer,Double,Long>> {
		private static final long serialVersionUID = 1L;

		private SetAccumulator<Double> counter = new SetAccumulator<Double>();
		
		@Override
		public void open(Configuration parameters) {
			this.getRuntimeContext().addAccumulator("counter-set", counter);
		}
		
		@Override
		public Tuple4<Integer, Integer, Double, Long> map(Tuple4<Integer, Integer, Double, Long> arg0)
				throws Exception {
			counter.add(arg0.f2);
			return arg0;
		}	
	}
	
